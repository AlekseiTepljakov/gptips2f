function y = mlf_a_a1_app(alpha, x)
%MLF_A_A1_APP Mittag-Leffler function (alpha, alpha+1) approximation

% Get the map
amap = get_alpha_map();

% Check alpha value
if alpha <= 0 || alpha >= 1 || ~isKey(amap, alpha)
    error('Unsupported value of alpha.');
end

% Check argument
if min(x)<0
    error('Argument must be positive.'); 
end

% Compute the output
avals = amap(alpha);
y = polyval(avals{1},-x) ./ polyval(avals{2},-x);

end

